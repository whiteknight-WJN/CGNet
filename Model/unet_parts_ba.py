class PACA(nn.Module):
    def __init__(self, in_channels):
        super().__init__()
        self.ca1=CALayer(in_channels)
        self.pa1=PALayer(in_channels)

        self.ca2 = CALayer(in_channels)
        self.pa2 = PALayer(in_channels)

    def forward(self, x1,x2):
        y1=self.ca1(x1)+self.ca2(x2)
        y2=self.pa1(x1)+self.pa2(x2)
        y=torch.cat([y1,y2],dim=1)
        return y

class Conv2DMod(nn.Module):
    def __init__(self, in_chan, out_chan, kernel, demod=True, stride=1, dilation=1):
        super().__init__()
        self.filters = out_chan
        self.demod = demod
        self.kernel = kernel
        self.stride = stride
        self.dilation = dilation
        self.adapmaxpooling=nn.AdaptiveMaxPool2d((self.kernel,self.kernel))
        self.weight = nn.Parameter(torch.randn((out_chan, in_chan, kernel, kernel)))
        nn.init.kaiming_normal_(self.weight, a=0, mode='fan_in', nonlinearity='leaky_relu')

    def _get_same_padding(self, size, kernel, dilation, stride):
        return ((size - 1) * (stride - 1) + dilation * (kernel - 1)) // 2

    def forward(self, x, y):
        b, c, h, w = x.shape
        y=self.adapmaxpooling(y)

        w1 = y[:, None, :, :, :]
        w2 = self.weight[None, :, :, :, :]
        weights = w2 * (w1 + 1)

        if self.demod:
            d = torch.rsqrt((weights ** 2).sum(dim=(2, 3, 4), keepdims=True) + 1e-5)
            weights = weights * d

        x = x.reshape(1, -1, h, w)

        _, _, *ws = weights.shape
        weights = weights.reshape(b * self.filters, *ws)

        padding = self._get_same_padding(h, self.kernel, self.dilation, self.stride)
        x = F.conv2d(x, weights, padding=padding, groups=b)

        x = x.reshape(-1, self.filters, h, w)
        return x


